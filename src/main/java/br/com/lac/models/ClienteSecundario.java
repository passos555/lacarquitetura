package br.com.lac.models;

/*
 * CLASSE AUXILIAR
 * */
public class ClienteSecundario {

	private String nomeClienteSec;
	private String emailClienteSec;
	private String telefoneClienteSec;
	private String cpfClienteSec;
	private String rgClienteSec;

	public String getNomeClienteSec() {
		return nomeClienteSec;
	}

	public void setNomeClienteSec(String nomeClienteSec) {
		this.nomeClienteSec = nomeClienteSec;
	}

	public String getEmailClienteSec() {
		return emailClienteSec;
	}

	public void setEmailClienteSec(String emailClienteSec) {
		this.emailClienteSec = emailClienteSec;
	}

	public String getTelefoneClienteSec() {
		return telefoneClienteSec;
	}

	public void setTelefoneClienteSec(String telefoneClienteSec) {
		this.telefoneClienteSec = telefoneClienteSec;
	}

	public String getCpfClienteSec() {
		return cpfClienteSec;
	}

	public void setCpfClienteSec(String cpfClienteSec) {
		this.cpfClienteSec = cpfClienteSec;
	}

	public String getRgClienteSec() {
		return rgClienteSec;
	}

	public void setRgClienteSec(String rgClienteSec) {
		this.rgClienteSec = rgClienteSec;
	}
}
